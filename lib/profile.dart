  TextEditingController carModel = TextEditingController();
  TextEditingController carNumber = TextEditingController();

  var emailText = "";
  var nameText = "";
  var ageText = "";
  var carModelText = "";
  var carNumberText = "";

  StreamSubscription subscription;

  getMyData() async {
    subscription = FirebaseDatabase.instance
        .reference()
        .child(widget.uid)
        .onValue
        .listen((event) async {
      var data = event.snapshot.value;
      setState(() {
        nameText = data['name'];
        ageText = data['age'];
        emailText = data['email'];
        carModelText = data['carModel'];
        carNumberText = data['carNumber'];
        email = TextEditingController(text: data['email']);
        name = TextEditingController(text: data['name']);
        age = TextEditingController(text: data['age']);
        carModel = TextEditingController(text: data['carModel']);
        carNumber = TextEditingController(text: data['carNumber']);
      });
    });
  }

  editMyData() async {
    FirebaseDatabase.instance.reference().child(widget.uid).update({
      "email": email.text ?? emailText,
      "name": name.text ?? nameText,
      "age": age.text ?? ageText,
      "carModel": carModel.text ?? carModelText,
      "carNumber": carNumber.text ?? carNumberText
    });
  }

  @override
  void initState() {
    super.initState();
    getMyData();
  }

  
